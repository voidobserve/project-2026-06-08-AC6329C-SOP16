static u8 __rf24g_get_key_val__(void)
{
	// 加入超时，防止丢包（超时时间与按键扫描时间有关）
	static volatile u8 timeout_cnt = 0;
	static u8 last_key_val = 0; // 上一次按键键值，在超时时间内返回上一次按键键值

	if (rf24g_key.is_update)
	{
		rf24g_key.is_update = 0;
		/*
			2.4G接收可能会丢失100~200ms的数据包（响应会慢一些）
			值 == 20，10ms调用一次该函数，这里填充200ms的超时值
		*/
		timeout_cnt = 20; 
		last_key_val = rf24g_key.key_val;
		return rf24g_key.key_val;
	}

	if (timeout_cnt)
	{
		// 如果还没有超时，返回上次检测到的键值
		timeout_cnt--;
		return last_key_val;
	}

	// 如果没有检测到按键，并且已经超时，返回 NO_KEY
	return NO_KEY;
}

/**
 * @brief 处理2.4G遥控器数据包
 *
 * 	在 le_gatt_client.c -> __resolve_adv_report() 中调用
 *
 */
void rf24g_driver_package_handle(void* ptr, u8 len)
{
	// printf_buf(ptr, len);

	if (len >= 10 &&
		((rf24g_remote_package_t*)ptr)->fix_val_1 == RF24G_TYPE_28KEY_FIXVAL_1 &&
		((rf24g_remote_package_t*)ptr)->fix_val_2 == RF24G_TYPE_28KEY_FIXVAL_2)
	{
		// printf_buf(ptr, len);
		// printf("key == %02x\n", (u16)((rf24g_remote_package_t*)ptr)->key_val);

		rf24g_key.key_val = (u8)((rf24g_remote_package_t*)ptr)->key_val;
		rf24g_key.is_update = 1;
	}
}

// 
/**
 * @brief 根据 按键键值 和 key_driver_scan 得到的事件值，转换为对应的按键事件
 *
 */
u8 rf24g_convert_to_key_event(u8 key_driver_value, u8 key_driver_event)
{
	// 将 key_driver_scan 得到的 key_event 转换成自定义的 key_event

	// 索引对应 key_event_table[][] 中的
	u8 key_event_index = 0; // 默认为0，0对应无效索引
	switch (key_driver_event)
	{
	case KEY_EVENT_PRESS:
		key_event_index = 1;
		break;
	case KEY_EVENT_LONG:
		key_event_index = 3;
		break;
	case KEY_EVENT_HOLD:
		key_event_index = 4;
		break;
	case KEY_EVENT_UP:
		key_event_index = 5;
		break;

	default:
		break;
	}

	if (0 == key_event_index || NO_KEY == key_driver_value)
	{
		// 按键事件与上面的事件都不匹配
		// 得到的键值是无效键值
		return RF24G_TYPE_28KEY_EVENT_NONE;
	}

	// 遍历表格中的每一个按键：
	for (u8 i = 0;
		i < sizeof(rf24g_key_type_28keys_table) /
		sizeof(rf24g_key_type_28keys_table[0]);
		i++)
	{
		if (key_driver_value == rf24g_key_type_28keys_table[i][0])
		{
			return rf24g_key_type_28keys_table[i][key_event_index];
		}
	}

	// 如果运行到这里，都没有找到对应的按键，返回无效按键事件
	return RF24G_TYPE_28KEY_EVENT_NONE;
}

